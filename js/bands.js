function loadBands() {
    var imgBand = '';
    var cont = 0;
    for (j = 1; j <= 3; j++) {
        imgBand += '<div class="row band-row">';

        for (i = 0; i < 3; i++) {
            imgBand += '<div class="col-lg-4 band-img">';
            imgBand += '<img src="' + jsonData.bands[cont+j].picture + '" alt="' + jsonData.bands[cont+j].name + '" title="' + jsonData.bands[cont+j].name + '" onclick="loadInfoBandImg(' + jsonData.bands[cont+j].id + ')"' + '>';
            imgBand += '</div>';
            cont = cont + 1;
        };
        
        imgBand += '</div>';
    }
    document.getElementById ('load-band-img').innerHTML = imgBand;
};

function loadInfoBandImg(id) {
	id = id-1;
	var infoBand = '';
	infoBand += '<div class="band-name"> <strong>' + jsonData.bands[id].name + '</strong> </div>';
	infoBand += '<div class="band-img-info">';
	infoBand += '<img src="' + jsonData.bands[id].picture + '" title="' + jsonData.bands[id].name + '">';
	infoBand += '<div class="band-description">' + jsonData.bands[id].description + '</div>';
    infoBand += '<div class="band-site">' + jsonData.bands[id].web_site + '</div>';
	//imgBand += '</div>';
	document.getElementById ('load-band-info').innerHTML = infoBand;
};

